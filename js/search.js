function Search() {
  const [searchTerm, setSearchTerm] = React.useState("");
  const [searchResults, setSearchResults] = React.useState([]); //handleChange with user input value

  const handleChange = e => {
    setSearchTerm(e.target.value);
  }; //useEffect hook combines lifecycle events (componentDidMount/Update/Unmount)


  React.useEffect(() => {
    const results = products.filter(product => //convert product to lower case to match searchTerm string
    product.name.toLowerCase().includes(searchTerm.toLowerCase())); //set matches into array of results

    setSearchResults(results);
  }, [searchTerm]);

  const categorise = e => {
    const results = products.filter(product => product.category.match(e));
    setSearchResults(results);
  };

  return React.createElement("div", {
    className: "search"
  }, React.createElement("div", {
    className: "filters flex-container"
  }, React.createElement("input", {
    className: "flex-item",
    type: "text",
    placeholder: "Search here...",
    value: searchTerm,
    onChange: handleChange
  }), React.createElement("div", {
    className: "categories flex-item flex-container flex-row"
  }, React.createElement("div", {
    key: "All",
    className: "selection flex-item",
    onClick: () => {
      categorise();
    }
  }, "All"), [...new Set(products.map(product => product.category))].map(category => React.createElement("div", {
    key: category,
    id: category,
    className: "selection flex-item",
    onClick: () => {
      categorise(category);
      document.getElementById(category).classList.add('toggle');
    }
  }, category)))), React.createElement("ul", {
    className: "flex-container flex-row"
  }, searchResults.map(product => React.createElement("li", {
    key: product.name,
    className: "flex-item"
  }, product.name))));
}

const rootElement = document.getElementById("root");
ReactDOM.render(React.createElement(Search, null), rootElement);